    private void Botao_criar_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Botao_criar_salvarActionPerformed
        String nome = Texto_criar_nome.getText();
        String cpf = Texto_criar_cpf.getText();
        String nConta = Texto_criar_cpf.getText();
        String senha = Texto_criar_senha.getText();
        String data = Texto_criar_nasc.getText();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate nasc = LocalDate.parse(data, formatter);
        ClienteDAO dao = new ClienteDAO();
        dao.Novo(new Cliente(nConta,senha,nome,cpf,nasc));
        
    }//GEN-LAST:event_Botao_criar_salvarActionPerformed

